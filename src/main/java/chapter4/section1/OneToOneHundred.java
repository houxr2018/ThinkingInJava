/**
 * Description: 打印从1到100的数字
 * CreateTime: 2017/10/12 16:34
 *
 * @author 王熙楠 wangxn@tiansu-china.com
 */
public class OneToOneHundred {

    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
    }
}
